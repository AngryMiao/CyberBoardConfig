const ledStrip = document.querySelector(".led-strip");
for (let i = 0; i < 200; i++) {
  const led = document.createElement("div");
  led.className = "led";

  const ledBg = document.createElement("div");
  ledBg.className = "led-bg";
  led.appendChild(ledBg);

  const ledCenter = document.createElement("div");
  ledCenter.className = "led-center";
  led.appendChild(ledCenter);

  ledStrip.appendChild(led);
}

