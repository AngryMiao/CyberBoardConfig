let index = -1;
function play() {
  index = index + 1;
  if (index >= frame.frames.length) index = 0;
  for (let i = 0; i < 200; i++) {
    const color = frame.frames[index].frameRGB[i];
    const led = document.getElementsByClassName("led")[i];
    const ledBg = led.getElementsByClassName("led-bg")[0];
    const ledCenter = led.getElementsByClassName("led-center")[0];
    ledBg.style.background = color;
    ledCenter.style.background = `radial-gradient(circle, ${color} 25%, transparent)`;
  }
}
setInterval(play, speedArray[frame.speed.value]);
